

/**
 * TypeCast
 */
public class TypeCast {

    public static void main(String[] args) {
       byte a = 30;
        int b = (int) a;
        short c = (short)b;
        double d = (double)c;
        float e = (float)d;
        System.out.println("Byte :" + a );
        System.out.println("Int :" + b );
        System.out.println("Short :" + c );
        System.out.println("Double :" + d );
        System.out.println("Float :" + e );
    
    }
}